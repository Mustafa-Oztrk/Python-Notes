def toplama(a, b):
    """
    Bu fonksiyon iki sayıyı toplar ve sonucu döndürür.
    
    Parametreler:
    a (int): Toplanacak ilk sayı
    b (int): Toplanacak ikinci sayı
    
    Returns:
    int: İki sayının toplamı
    """
    return a + b

toplama(5, 10) # 15 döner
